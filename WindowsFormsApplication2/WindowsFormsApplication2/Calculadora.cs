using System;
using System.Collections.Generic;
using System.Text;

namespace WindowsFormsApplication2
{
    class Calculadora
    {
        public int suma(int a, int b)
        {
            return a + b;
        }
        public int resta(int a, int b)
        {
            return a - b;
        }
        public int multi(int a, int b)
        {
            return a * b;
        }
        public Double divide(int a, int b)
        {
            return a / b;
        }
    }
}
